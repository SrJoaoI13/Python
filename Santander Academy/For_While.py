print("Numeros de 1 a 5 multiplicados por 2")
for numero in range(1, 6):  # gera números de 1 até 5
    print(numero * 2) # multiplica cada número por 2 e imprime

print("Numeros de 1 a 5 mulplicados por 2 ")
contador = 1 
while contador <= 5: # repete enquanto contador for menor ou igual a 5
    print(contador * 2) # imprime o contador multiplicado por 2
    contador +=1   # soma +1 ao contador a cada volta